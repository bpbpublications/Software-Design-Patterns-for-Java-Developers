package edu.bpb.oops.pattern.ch7.decorator;

/**
 * @author Lalit Mehra
 *
 */
public class MapCoordinates {

	private float x;
	private float y;
	
	public MapCoordinates(float x, float y) {
		this.x = x;
		this.y = y;
	}
	
	public float getX() {
		return x;
	}
	
	public float getY() {
		return y;
	}
	
}
